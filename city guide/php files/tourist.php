Bangalore, also known as Bengaluru (Kannada) is the capital of the Indian State of Karnataka.
 Bangalore is nicknamed the Garden City and was once called a Pensioner's Paradise. 
 Located on the Deccan Plateau in the south-eastern part of Karnataka, Bangalore is India's third most populous city.
  There are a number of expats from across the world living in the city, thanks to the growing presence of Multi-National Companies.
  Bangalore is known as the Silicon Valley of India because of its position as the nation's leading IT exporter.
  <P><b>CLIMATE</b></p>
  <P>Bengaluru has pleasant summers and mild winters. Summer temperatures average in the low to mid-90s F (about 34 °C), and winter temperatures rarely drop below 60 °F (16 °C). The city receives about 36 inches (914 mm) of annual rainfall, which has been inadequate as a water supply for its increasing population and industry.
   Most of the city’s water comes from the Kaveri (Cauvery) River, about 45 miles (70 km) to the south.</P>
   <p><b>LANGUAGE</b></P>
   <P> Kannada is the official language of Bangalore, but the city is multi-cultural.
    According to the 2011 Census, Kannada is spoken as a first language by 46% of the population, Tamil by 13.99%, Telugu by 13.89%, Urdu by 12%, Hindi by 5.4%, Malayalam by 2.8%, Marathi by 1.8%, Konkani by 0.67%, Bengali by 0.64%, Odia by 0.52%, Tulu by 0.49%, Gujarati by 0.47% and other languages by 1.33%.
    As in the rest of the state, Kannada is the most widely spoken language, but English is a commonly spoken second language in the city.</P>
